  <h1>Ricardo Pholenz</h1>
  <div class="about"><p>Es poeta y escritor. Es el mayor de cinco hijos, todos varones,
su padre es ingeniero mecánico electricista y su madre estudió
comercio, es una gran administradora. Tiene una predilección
especial por la comida oriental, en especial los tallarines y los
rollos primavera, tal vez como evocación y homenaje de la segunda
secuencia de Blade Runner. Una magdelena proustiana como puede serlo
también el Toma 5 del Cuarteto de Dave Brubeck, la secuencia de los
elefantes rosados en Dumbo, el sabor de los cruasanes (que puede
llevarlo a las lágrimas), las galletas de jengibre.</p>

  </div>
